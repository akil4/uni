package uni.array;
class Solution
{
    public static void reverseArray(int arr[])
    {
        for(int i=arr.length;i>0;i--)
        {
          System.out.print(i+" ");
        }
        
    
        }
        public static void main(String[]args)
        {
         int[] a={1,2,3,4,5};
         reverseArray(a);
        }
    }
